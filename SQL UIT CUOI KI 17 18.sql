FROM (
	SELECT MAKH, COUNT(*) AS SLLC
	FROM HOADON 
	JOIN CTHD ON HOADON.SOHD = CTHD.SOHD
	GROUP BY MAKH
	) AS G
WHERE G.SLLC = ( SELECT COUNT(*) FROM LOAICAY)

