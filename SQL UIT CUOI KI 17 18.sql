SELECT LOAICAY.MALC, TENLC, SUM(SOLUONG) AS SOLUONG, RANK() OVER(ORDER BY SUM(SOLUONG) ASC) AS RANK
FROM LOAICAY
INNER JOIN CTHD ON CTHD.MALC = LOAICAY.MALC
